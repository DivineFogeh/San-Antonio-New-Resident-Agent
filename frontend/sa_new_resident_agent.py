# ── Cell 4 · Validation Helpers ───────────────────────────────────────────────
def validate_field(field, value):
    """Returns an error string, or None if valid."""
    v = value.strip()
    if field.get('required') and not v:
        return f"{field['label']} is required"
    if not v:
        return None
    pattern = field.get('pattern')
    if pattern and not re.match(pattern, v):
        if field['id'] == 'ssn': return 'Enter exactly 4 digits'
        if field['id'] == 'zip': return 'Enter a valid 5-digit ZIP'
        return 'Invalid format'
    if field['type'] == 'email':
        if not re.match(r'^[^\s@]+@[^\s@]+\.[^\s@]+$', v):
            return 'Enter a valid email address'
    if field['type'] == 'tel':
        if len(re.sub(r'\D', '', v)) < 7:
            return 'Enter a valid phone number'
    return None

def validate_step(fields, data):
    """Returns {field_id: error_msg}. Empty dict means all fields are valid."""
    return {f['id']: e for f in fields if (e := validate_field(f, data.get(f['id'], '')))}

