# ── Cell 5 · AI Chat Engine (connected to SA Resident Agent Backend) ──────────

def chat_with_agent(user_message):
    """Send a message to the backend AI agent and get a RAG-grounded response."""
    session['chat_history'].append({'role': 'user', 'content': user_message})

    try:
        response = requests.post(
            f"{BACKEND_URL}/api/services/agent/chat",
            json={"session_id": SESSION_ID, "message": user_message},
            timeout=30
        )
        response.raise_for_status()
        data = response.json()
        reply = data["data"]["reply"]

        # Sync checklist state from agent response
        if "checklist" in data["data"]:
            session['agent_checklist'] = data["data"]["checklist"]

    except requests.exceptions.ConnectionError:
        reply = "⚠️ Backend not reachable. Make sure docker compose is running at localhost:8000"
    except requests.exceptions.Timeout:
        reply = "⚠️ Request timed out. The AI agent may be busy — try again."
    except Exception as e:
        reply = f"⚠️ Error: {str(e)}"

    session['chat_history'].append({'role': 'assistant', 'content': reply})
    return reply

def submit_form_to_backend(service_id, form_data):
    """Submit completed form data to the backend."""
    try:
        response = requests.post(
            f"{BACKEND_URL}/api/services/submit/{service_id}",
            json={"user_id": USER_ID, "service": service_id, "form_data": form_data},
            timeout=15
        )
        response.raise_for_status()
        return response.json()
    except Exception as e:
        return {"status": "error", "detail": str(e)}

def sync_checklist_to_backend(service_id, step, completed):
    """Sync form completion status to the backend checklist."""
    try:
        requests.patch(
            f"{BACKEND_URL}/api/checklist/{USER_ID}",
            json={"service": service_id, "step": step, "completed": completed},
            timeout=10
        )
    except Exception as e:
        print(f"⚠️ Checklist sync failed: {e}")

